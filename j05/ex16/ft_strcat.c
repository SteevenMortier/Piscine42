char	*ft_strcat(char *dest, char *src)
{
	int		i;
	int		y;

	y = -1;
	i = 0;
	while (dest[i] != '\0')
		i++;
	while (src[++y])
	{
		dest[i + y] = src[y];
	}
	dest[i + y] = '\0';
	return (dest);
}
