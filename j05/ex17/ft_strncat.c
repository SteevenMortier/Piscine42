char	*ft_strncat(char *dest, char *src, int nb)
{
	int		i;
	int		y;

	y = -1;
	i = 0;
	while (dest[i] != '\0')
		i++;
	while (++y < nb)
	{
		dest[i + y] = src[y];
	}
	dest[i + y] = '\0';
	return (dest);
}
